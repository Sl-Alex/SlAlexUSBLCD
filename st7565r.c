static void spi_setup(void)
{
    /* Enable clocks */
    rcc_periph_clock_enable(RCC_GPIOA);
    rcc_periph_clock_enable(RCC_AFIO);
    rcc_periph_clock_enable(RCC_SPI1);
    rcc_periph_clock_enable(RCC_DMA1);

    gpio_set_mode(GPIOA, GPIO_MODE_OUTPUT_50_MHZ,
            GPIO_CNF_OUTPUT_ALTFN_PUSHPULL, GPIO5 | GPIO7 );

    gpio_set_mode(ST7565R_PIN_GPIO, GPIO_MODE_OUTPUT_2_MHZ,
              GPIO_CNF_OUTPUT_PUSHPULL, ST7565R_PIN_CS);
    gpio_set_mode(ST7565R_PIN_GPIO, GPIO_MODE_OUTPUT_2_MHZ,
              GPIO_CNF_OUTPUT_PUSHPULL, ST7565R_PIN_RST | ST7565R_PIN_RS);
              
    gpio_set(ST7565R_PIN_GPIO, ST7565R_PIN_CS);

    spi_reset(SPI1);

    SPI1_I2SCFGR = 0;

    spi_init_master(SPI1, SPI_CR1_BAUDRATE_FPCLK_DIV_64, SPI_CR1_CPOL_CLK_TO_0_WHEN_IDLE,
            SPI_CR1_CPHA_CLK_TRANSITION_1, SPI_CR1_DFF_8BIT, SPI_CR1_MSBFIRST);

    spi_enable_software_slave_management(SPI1);
    spi_set_nss_high(SPI1);

    spi_enable(SPI1);
}

void st7565r_init(uint8_t contrast) {
    spi_setup();

    gpio_set(ST7565R_PIN_GPIO, ST7565R_PIN_CS);

    gpio_clear(ST7565R_PIN_GPIO, ST7565R_PIN_RST);
    delay_ms(10);
    gpio_set(ST7565R_PIN_GPIO, ST7565R_PIN_RST);
    delay_ms(10);

    // LCD bias select
    st7565r_command(CMD_SET_BIAS_7);
    // ADC select
    st7565r_command(CMD_SET_ADC_REVERSE);
    // SHL select
    st7565r_command(CMD_SET_COM_NORMAL);
    // Initial display line
    st7565r_command(CMD_SET_DISP_START_LINE);

    // turn on voltage converter (VC=1, VR=0, VF=0)
    st7565r_command(CMD_SET_POWER_CONTROL | 0x4);
    delay_ms(50);

    // turn on voltage regulator (VC=1, VR=1, VF=0)
    st7565r_command(CMD_SET_POWER_CONTROL | 0x6);
    delay_ms(50);

    // turn on voltage follower (VC=1, VR=1, VF=1)
    st7565r_command(CMD_SET_POWER_CONTROL | 0x7);
    delay_ms(10);

    // set lcd operating voltage (regulator resistor, ref voltage resistor)
    st7565r_command(CMD_SET_RESISTOR_RATIO | 0x6);
    delay_ms(10);

    st7565r_command(CMD_DISPLAY_ON);
    delay_ms(10);
    st7565r_command(CMD_SET_ALLPTS_NORMAL);
    delay_ms(10);

    st7565r_set_brightness(contrast);
    delay_ms(10);
}

inline void st7565r_spiwrite(uint8_t c) {
    gpio_clear(ST7565R_PIN_GPIO, ST7565R_PIN_CS);

    spi_send(SPI1, c);

    while (!(SPI_SR(SPI1) & SPI_SR_TXE));
    while (SPI_SR(SPI1) & SPI_SR_BSY);
    gpio_set(ST7565R_PIN_GPIO,ST7565R_PIN_CS);
}

void st7565r_spiwrite_buf(uint8_t *buf, uint16_t sz)
{
    gpio_clear(ST7565R_PIN_GPIO, ST7565R_PIN_CS);
    for (int i = 0; i < sz; i++)
        spi_send(SPI1, buf[i]);

    while (!(SPI_SR(SPI1) & SPI_SR_TXE));
    while (SPI_SR(SPI1) & SPI_SR_BSY);
    gpio_set(ST7565R_PIN_GPIO,ST7565R_PIN_CS);
}

void st7565r_command(uint8_t c) {
    gpio_clear(ST7565R_PIN_GPIO, ST7565R_PIN_RS);
    st7565r_spiwrite(c);
}

void st7565r_data(uint8_t c) {
    gpio_set(ST7565R_PIN_GPIO, ST7565R_PIN_RS);
    st7565r_spiwrite(c);
}

void st7565r_set_brightness(uint8_t val) {
    st7565r_command(CMD_SET_VOLUME_FIRST);
    st7565r_command(CMD_SET_VOLUME_SECOND | (val & 0x3f));
}

void st7565r_set_raw_data(uint32_t offset, uint8_t *data, uint32_t sz)
{
    for (uint32_t i = 0; i < sz; i++)
    {
        st7565r_buffer[offset+i] = data[i];
    }
}


void st7565r_display(void) {
    uint8_t col, maxcol, p;

    for(p = 0; p < 8; p++)
    {
        st7565r_command(CMD_SET_PAGE | pagemap[p]);
        delay_ms(2);

        // start at the beginning of the row
        col = 0;
        maxcol = LCDWIDTH;

        st7565r_command(CMD_SET_COLUMN_LOWER | ((col+ST7565R_STARTBYTES) & 0xf));
        delay_ms(2);
        st7565r_command(CMD_SET_COLUMN_UPPER | (((col+ST7565R_STARTBYTES) >> 4) & 0x0F));
        delay_ms(2);
        st7565r_command(CMD_RMW);
        delay_ms(2);

        for(; col < maxcol; col++)
        {
            st7565r_data(st7565r_buffer[(128*p)+col]);
        }
        delay_ms(1);
    }
}

void st7565r_clear(void) {
    memset(st7565r_buffer, 0, 1024);
}


void st7565r_clear_display(void) {
    uint8_t p, c;

    for(p = 0; p < 8; p++)
    {
        st7565r_command(CMD_SET_PAGE | p);
        for(c = 0; c < 128; c++)
        {
            st7565r_command(CMD_SET_COLUMN_LOWER | (c & 0xf));
            st7565r_command(CMD_SET_COLUMN_UPPER | ((c >> 4) & 0xf));
            st7565r_data(0x0);
        }     
    }
}

